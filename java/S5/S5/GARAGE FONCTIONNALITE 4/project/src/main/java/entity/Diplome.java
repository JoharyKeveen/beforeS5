package entity;

/**
 *
 * @author ITU
 */
public class Diplome {
    private int id;
    private String nom;

    public Diplome() {
    }

    public Diplome(int id, String nom) {
        this.id = id;
        this.nom = nom;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
